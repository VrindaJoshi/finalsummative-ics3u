
public class City {
	
	String planet;
	String continent;
	String country;
	String name;
	int population;
	
	public City(String planet, String continent, String country, String name, int population) {
		super();
		this.planet = planet;
		this.continent = continent;
		this.country = country;
		this.name = name;
		this.population = population;
	}

}
